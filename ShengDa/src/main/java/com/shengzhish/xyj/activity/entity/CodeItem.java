public class CodeItem {
    private String id;
    private String name;
    private String img;
    private String desc;
    private int isGot;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getIsGot() {
        return isGot;
    }

    public void setIsGot(int isGot) {
        this.isGot = isGot;
    }

}