package feipai.qiangdan.javabean;

/**
 * 个人信息
 */
public class MyInfoBean {
    private String name;
    private int endOrderNum;
    private String allIncome;
    private String monthIncome;
    private String yDayIncome;
    private String myMoney;
    private String status;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEndOrderNum() {
        return endOrderNum;
    }

    public void setEndOrderNum(int endOrderNum) {
        this.endOrderNum = endOrderNum;
    }

    public String getAllIncome() {
        return allIncome;
    }

    public void setAllIncome(String allIncome) {
        this.allIncome = allIncome;
    }

    public String getMonthIncome() {
        return monthIncome;
    }

    public void setMonthIncome(String monthIncome) {
        this.monthIncome = monthIncome;
    }

    public String getyDayIncome() {
        return yDayIncome;
    }

    public void setyDayIncome(String yDayIncome) {
        this.yDayIncome = yDayIncome;
    }

    public String getMyMoney() {
        return myMoney;
    }

    public void setMyMoney(String myMoney) {
        this.myMoney = myMoney;
    }
    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}

